type SentenceOverrideLocale = "fa" | "ar";

type SentenceOverrideMap = Record<
  string,
  Partial<Record<SentenceOverrideLocale, string>>
>;

function setOverride(
  target: SentenceOverrideMap,
  prefix: string,
  stemIndex: number,
  tailIndex: number,
  fa: string,
  ar: string,
): void {
  target[`${prefix}-${stemIndex + 1}-${tailIndex + 1}`] = { fa, ar };
}

function addSovGroup(
  target: SentenceOverrideMap,
  prefix: string,
  faSubjects: string[],
  faVerbs: string[],
  faTails: string[],
  arSubjects: string[],
  arVerbs: string[],
  arTails: string[],
): void {
  for (let stemIndex = 0; stemIndex < faSubjects.length; stemIndex += 1) {
    for (let tailIndex = 0; tailIndex < faTails.length; tailIndex += 1) {
      setOverride(
        target,
        prefix,
        stemIndex,
        tailIndex,
        `${faSubjects[stemIndex]} ${faTails[tailIndex]} ${faVerbs[stemIndex]}`.trim(),
        `${arSubjects[stemIndex]} ${arVerbs[stemIndex]} ${arTails[tailIndex]}`.trim(),
      );
    }
  }
}

function addClauseGroup(
  target: SentenceOverrideMap,
  prefix: string,
  faStems: string[],
  faTails: string[],
  arStems: string[],
  arTails: string[],
): void {
  for (let stemIndex = 0; stemIndex < faStems.length; stemIndex += 1) {
    for (let tailIndex = 0; tailIndex < faTails.length; tailIndex += 1) {
      setOverride(
        target,
        prefix,
        stemIndex,
        tailIndex,
        `${faStems[stemIndex]} ${faTails[tailIndex]}`.trim(),
        `${arStems[stemIndex]} ${arTails[tailIndex]}`.trim(),
      );
    }
  }
}

function addConditionalGroup(
  target: SentenceOverrideMap,
  prefix: string,
  faConditions: string[],
  faResults: string[],
  arConditions: string[],
  arResults: string[],
): void {
  for (let stemIndex = 0; stemIndex < faConditions.length; stemIndex += 1) {
    for (let tailIndex = 0; tailIndex < faResults.length; tailIndex += 1) {
      setOverride(
        target,
        prefix,
        stemIndex,
        tailIndex,
        `${faConditions[stemIndex]}، ${faResults[tailIndex]}`,
        `${arConditions[stemIndex]}، ${arResults[tailIndex]}`,
      );
    }
  }
}

function addMatrixGroup(
  target: SentenceOverrideMap,
  prefix: string,
  faSubjects: string[],
  faTailMatrix: string[][],
  arSubjects: string[],
  arTailMatrix: string[][],
): void {
  for (let stemIndex = 0; stemIndex < faSubjects.length; stemIndex += 1) {
    for (let tailIndex = 0; tailIndex < faTailMatrix.length; tailIndex += 1) {
      setOverride(
        target,
        prefix,
        stemIndex,
        tailIndex,
        `${faSubjects[stemIndex]} ${faTailMatrix[tailIndex][stemIndex]}`.trim(),
        `${arSubjects[stemIndex]} ${arTailMatrix[tailIndex][stemIndex]}`.trim(),
      );
    }
  }
}

